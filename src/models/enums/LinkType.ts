/**
 * Enum representing different types of links.
 */
export enum LinkType {
  /**
   * Link type for graph links.
   */
  GRAPH_LINK = 'GRAPH',

  /**
   * Link type for class links.
   */
  CLASS_LINK = 'CLASS',

  /**
   * Link type for profile links.
   */
  PROFILE_LINK = 'PROFILE',

  /**
   * Link type for chat links.
   */
  CHAT_LINK = 'CHAT',
}
